	public String getMIMEType(File f){
		String end = f.getName()
				.substring(f.getName().lastIndexOf(".")+1,
						f.getName().length()).toLowerCase();
		String type="";
		
		if(end.equals("mp3")){
			type="audio";
		}else if(end.equals("jpg")){
			type="image";
		}else if(end.equals("mp4")){
			type="video";
		}else{
			type="*";
		}
		type+="/*";
		
		return type;
	}
	
	public static boolean isVideoType(String f){
		String end = f.substring(f.lastIndexOf(".")+1,f.length()).toLowerCase();

		for(int i=0;i<VIDEO_EXTENSIONS.length;i++){
			if(end.equals(VIDEO_EXTENSIONS[i])){
				return true;
			}
		}
		return false;
	}
	
	/**时间格式转换*/
	public static String toTime(int time){
		time /= 1000;
		int minute = time/60;
		int hour = minute/60;
		int second = time%60;
		minute %= 60;
		
		return String.format("%02d:%02d:%02d",hour,minute,second);
	}
	
	/*判读是否有网络连接*/
	public static boolean isNetworkConnected(Context context) {  
	     if (context != null) {  
	         ConnectivityManager mConnectivityManager = (ConnectivityManager) context  
	                 .getSystemService(Context.CONNECTIVITY_SERVICE);  
	         NetworkInfo mNetworkInfo = mConnectivityManager.getActiveNetworkInfo();  
	         if (mNetworkInfo != null) {  
	             return mNetworkInfo.isAvailable();  
	         }  
	     }  
	     return false;  
	 }
